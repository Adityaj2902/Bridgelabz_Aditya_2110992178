// Summary of Scheduling Methods
// setTimeout(): Executes a function once after a specified delay.
// setInterval(): Executes a function repeatedly at specified intervals.
// requestAnimationFrame(): Schedules a function to run before the next repaint, ideal for animations.
// async/await with Promise: Allows you to delay or pause execution in an asynchronous manner.
// These methods provide flexibility for controlling the timing and execution flow in your JavaScript programs.



// setTimeout(function, delay, arg1, arg2, ...);
// function: The function to execute.
// delay: The time (in milliseconds) to wait before executing the function.
// arg1, arg2, ...: Optional arguments that will be passed to the function when it's called.

// setInterval(function, interval, arg1, arg2, ...);

// function: The function to execute.
// interval: The time (in milliseconds) between each execution.
// arg1, arg2, ...: Optional arguments passed to the function.

